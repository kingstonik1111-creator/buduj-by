-- 1) Убираем у анонимов доступ ко всей таблице целиком...
REVOKE SELECT ON public.profiles FROM anon;

-- 2) ...и возвращаем доступ ТОЛЬКО к безопасным колонкам
--    (здесь НЕТ phone, auth_email, telegram_chat_id, verified_doc_url)
GRANT SELECT (
  id, created_at, role, name, first_name, last_name,
  city, spec, bio, subscription_plan, subscription_end,
  rating, reviews_count, avatar_url, last_seen_at,
  is_verified, verified_status
) ON public.profiles TO anon;

-- Залогиненные (authenticated) сохраняют полный доступ к своим
-- и видимым профилям — отдельно ничего делать не нужно.

